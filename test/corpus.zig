//! On-disk sample-corpus harness (NFR-TEST-2).
//!
//! All other round-trips in the suite are synthetic and in-memory. This harness instead
//! commits a small set of *real* `.fits` files under `test/corpus/` and asserts that the
//! public `Fits.openFile` API can re-open each from disk and recover both its header
//! structure and its data byte-for-byte.
//!
//! The set is representative of the FITS structural zoo:
//!   1. `primary_image.fits`      — a primary IMAGE HDU (BITPIX 16, 2-D).
//!   2. `image_extension.fits`    — empty primary + an IMAGE extension (BITPIX -32, 2-D).
//!   3. `ascii_table.fits`        — primary + an ASCII `TABLE` (I/F/E columns).
//!   4. `binary_table.fits`       — primary + a `BINTABLE` (I/J/E/A columns).
//!   5. `binary_table_vla.fits`   — primary + a `BINTABLE` with a `1PJ` variable-length column.
//!   6. `compressed_image.fits.gz`— a whole-file GZIP-compressed primary IMAGE.
//!
//! The files are generated by the library's own writers (pure-Zig satisfies the structural
//! intent of NFR-TEST-2; the externally-authored CFITSIO/Astropy golden subset is the
//! separate blocked item). Generation is deterministic, so the committed bytes are stable.
//!
//! Hermeticity: the harness reads from the committed `test/corpus/` path under the build
//! root (the cwd of `zig build test`). A member is (re)generated only when its file is
//! missing — on CI the committed files are read directly. No network or external tools are
//! used. The committed files were produced by running this very harness once with the
//! directory empty.
const std = @import("std");
const fits = @import("zigfitsio");

const Allocator = std.mem.Allocator;
const KeywordValue = fits.KeywordValue;

const corpus_dir = "test/corpus";

// ── Header builders (public-API mirrors of the per-module test helpers) ──────────────────

// Append the eight mandatory table-structure cards shared by ASCII and binary tables.
fn appendTableSpine(
    h: *fits.Header,
    alloc: Allocator,
    xtension: []const u8,
    naxis1: u64,
    nrows: u64,
    pcount: u64,
    tfields: u64,
) !void {
    try h.appendValue(alloc, "XTENSION", .{ .string = xtension }, null);
    try h.appendValue(alloc, "BITPIX", .{ .int = 8 }, null);
    try h.appendValue(alloc, "NAXIS", .{ .int = 2 }, null);
    try h.appendValue(alloc, "NAXIS1", .{ .int = @intCast(naxis1) }, null);
    try h.appendValue(alloc, "NAXIS2", .{ .int = @intCast(nrows) }, null);
    try h.appendValue(alloc, "PCOUNT", .{ .int = @intCast(pcount) }, null);
    try h.appendValue(alloc, "GCOUNT", .{ .int = 1 }, null);
    try h.appendValue(alloc, "TFIELDS", .{ .int = @intCast(tfields) }, null);
}

fn kw(buf: []u8, comptime prefix: []const u8, n: usize) []const u8 {
    return std.fmt.bufPrint(buf, prefix ++ "{d}", .{n}) catch unreachable;
}

// ── Member 1: a primary IMAGE HDU ────────────────────────────────────────────────────────

const img1_axes = [_]u64{ 4, 3 }; // 4 fast × 3 slow = 12 pixels
fn img1_pixel(i: usize) i16 {
    return @as(i16, @intCast(i)) * 7 - 11; // deterministic, signed, spans zero
}

fn genPrimaryImage(alloc: Allocator, path: []const u8) !void {
    var f = try fits.Fits.createFile(alloc, path, .{});
    defer f.deinit();
    const hdu = try f.appendImageHdu(.{ .bitpix = 16, .axes = &img1_axes });
    var view = try fits.ImageView.of(&f, hdu);
    var pix: [12]i16 = undefined;
    for (&pix, 0..) |*p, i| p.* = img1_pixel(i);
    try view.writeAll(i16, &pix, .{});
    try f.flush();
}

fn verifyPrimaryImage(alloc: Allocator, path: []const u8) !void {
    var f = try fits.Fits.openFile(alloc, path, .read_only, .{});
    defer f.deinit();
    try std.testing.expectEqual(@as(usize, 1), try f.hduCount());
    const hdu = try f.select(1);
    try std.testing.expectEqual(@as(i64, 16), try hdu.header.getValue(i64, "BITPIX"));
    try std.testing.expectEqual(@as(i64, 2), try hdu.header.getValue(i64, "NAXIS"));
    try std.testing.expectEqual(@as(i64, 4), try hdu.header.getValue(i64, "NAXIS1"));
    try std.testing.expectEqual(@as(i64, 3), try hdu.header.getValue(i64, "NAXIS2"));
    var view = try fits.ImageView.of(&f, hdu);
    var pix: [12]i16 = undefined;
    try view.readAll(i16, &pix, .{});
    for (pix, 0..) |got, i| try std.testing.expectEqual(img1_pixel(i), got);
}

